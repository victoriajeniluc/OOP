/**
 * Use of final modifier with methods.
 *
 * @author Udayan Khattry
 */


public class Super {
    //final method
    public final void m1() {
        System.out.println("Super: m1");
    }

    //Non-final method
    public void m2() {
        System.out.println("Super: m2");
    }
}
